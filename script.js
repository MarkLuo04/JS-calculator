var x = 4;
var y = 3;

// Addition
function add(x, y) {
    return x + y;
}
console.log(add(x, y));

// Subtraction
function subtract(x, y) {
    return x - y;
}
console.log(subtract(x, y));

// Multiplication
function multiply(x, y) {
    return x * y;
}
console.log(multiply(x, y));

function divide(x, y) {
    return x / y;
}
console.log(divide(x, y));